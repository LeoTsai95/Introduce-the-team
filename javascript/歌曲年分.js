var timelineContainer = document.getElementById("timelineContainer");

// 顯示時間軸的函式
function displayTimeline() {
  timelineContainer.innerHTML = ""; // 清空時間軸容器

  for (var i = 0; i < songyear.length; i++) {
    var item = songyear[i];

    var timelineItem = document.createElement("div");
    timelineItem.className = "timeline-item";
    timelineItem.setAttribute("data-date", item.date);

    var content = document.createElement("div");
    content.className = "content";

    var title = document.createElement("h3");
    title.textContent = item.title;
    title.classList.add("title");

    var novel = document.createElement("p");
    novel.textContent = item.novel;
    novel.classList.add("novel");

    var category = document.createElement("p");
    category.textContent = item.category;
    category.classList.add("category");

    if (item.category === "THE BOOK") {
      timelineItem.classList.add("category-THEBOOK");
      title.classList.add("category-THEBOOK");
      novel.classList.add("category-THEBOOK");
      category.classList.add("category-THEBOOK");
    } else if (item.category === "E-SIDE") {
      timelineItem.classList.add("category-ESIDE");
      title.classList.add("category-ESIDE");
      novel.classList.add("category-ESIDE");
      category.classList.add("category-ESIDE");
    } else if (item.category === "其他單曲") {
      timelineItem.classList.add("category-其他單曲");
      title.classList.add("category-其他單曲");
      novel.classList.add("category-其他單曲");
      category.classList.add("category-其他單曲");
    }

    var videoLink = document.createElement("a");
    videoLink.href = "https://www.youtube.com/watch?v=" + item.videoID;
    videoLink.target = "_blank"; // 開啟連結於新分頁

    var image = document.createElement("img");
    image.src = item.image;
    image.alt = item.title;
    image.classList.add("timeline-image");

    // 應用 hover 效果
    image.addEventListener("mouseover", function (event) {
      this.style.transform = "scale(1.1)";
    });

    image.addEventListener("mouseout", function (event) {
      this.style.transform = "scale(1)";
    });

    videoLink.appendChild(image);

    content.appendChild(title);
    content.appendChild(novel);
    content.appendChild(category);
    content.appendChild(videoLink);

    timelineItem.appendChild(content);
    timelineContainer.appendChild(timelineItem);
  }
}



// 倒序排列陣列的函式
function reverseArray() {
  songyear.reverse();
  displayTimeline(); // 重新顯示時間軸
}

// 初始顯示時間軸
displayTimeline();
reverseArray(); // 倒序排列数组


